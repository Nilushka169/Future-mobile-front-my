const API_URL = process.env.REACT_APP_API_URL || "http://localhost:5000/api";

export const productInformation = [
];

export function getInfoById(infoId) {
  const n = Number(infoId);
  if (Number.isNaN(n)) return undefined;
  return productInformation.find((p) => p.infoId === n);
}

(async function replaceWithBackendProductInformation() {
  try {
    const res = await fetch(`${API_URL}/product-information`);
    if (!res.ok) {
      console.warn(
        `productInformation.js: backend responded ${res.status} — keeping fallback data`
      );
      return;
    }

    const json = await res.json();

    const payload = Array.isArray(json) ? json : (Array.isArray(json?.data) ? json.data : null);

    if (!Array.isArray(payload) || payload.length === 0) {
      console.warn(
        "productInformation.js: backend returned no array — keeping fallback data"
      );
      return;
    }

    const normalized = payload.map((doc) => {
      const item = doc && typeof doc.toObject === "function" ? doc.toObject() : doc;

      const infoId = (() => {
        if (typeof item.infoId === "number") return item.infoId;
        if (typeof item.infoId === "string" && item.infoId.trim() !== "") {
          const n = Number(item.infoId);
          return Number.isNaN(n) ? undefined : n;
        }
        return undefined;
      })();

      const sections = Array.isArray(item.sections)
        ? item.sections.map((s) => ({
            topic: typeof s.topic === "string" ? s.topic : "",
            items: Array.isArray(s.items)
              ? s.items.map((it) => ({
                  subtopic: typeof it.subtopic === "string" ? it.subtopic : "",
                  detail: typeof it.detail === "string" ? it.detail : "",
                }))
              : [],
          }))
        : [];

      return {
        infoId: !Number.isNaN(infoId) ? infoId : undefined,
        sections,
        createdAt: item.createdAt,
        updatedAt: item.updatedAt,
        __raw: item,
      };
    });

    const valid = normalized.filter(
      (n) => typeof n.infoId === "number" && Array.isArray(n.sections)
    );

    if (valid.length === 0) {
      console.warn(
        "productInformation.js: backend data invalid — keeping fallback data"
      );
      return;
    }

    productInformation.length = 0;
    productInformation.push(...valid);

    console.log(
      `productInformation.js: replaced fallback with ${valid.length} items from backend`
    );
  } catch (err) {
    console.warn(
      "productInformation.js: failed to fetch backend productInformation — keeping fallback data",
      err
    );
  }
})();
